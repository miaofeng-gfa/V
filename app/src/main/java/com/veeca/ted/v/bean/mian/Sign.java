public class Sign {
    private String msg;
    private int gisign;
    private int ginum;
    private int inum;
    private boolean success;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getGisign() {
        return gisign;
    }

    public void setGisign(int gisign) {
        this.gisign = gisign;
    }

    public int getGinum() {
        return ginum;
    }

    public void setGinum(int ginum) {
        this.ginum = ginum;
    }

    public int getInum() {
        return inum;
    }

    public void setInum(int inum) {
        this.inum = inum;
    }

    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }
}
